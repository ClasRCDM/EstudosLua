-- Operadores Aritméticos --
-- + == Adição, 4+4, a+b
-- - == Subtração = 2-4, 4-r
-- * == Multiplicação = 4 * 2
-- / == Divisão = 5 / 5
-- - == Negação = -b, -6
-- ^ == Exponenciação = x^2, 3^0.5
print(22^0.5)

-- Operadores Relacionas --
-- < == Menor que
-- > == Maior que
-- <= == Menor ou igual
-- >= == Maior ou igual
-- == == Igual
-- ~= == Diferente | Python == !=
print(2<4)
print(4>2)
print(2==4)

-- Operadores Lógicos --
-- not true == false
-- a and b == true
-- a or b == false
print(true and false)
print(true or false)

-- Operadore de Concatenação --
-- .. == Concatenar
print('po ' .. 'mano')

nome, sobrenome = 'aa', 'bb'
print(nome.." - "..sobrenome)

print(#'Nome') -- # diz a quantidade de caracterez

t = {1, 3, 54, 6, 7}
print(#t)
